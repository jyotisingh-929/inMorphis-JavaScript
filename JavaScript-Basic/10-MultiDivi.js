function multiply() {
    n1 = document.getElementById("firstNumber").value;
    n2 = document.getElementById("secondNumber").value;

    document.getElementById("result").innerHTML = n1 * n2;
}
function division() {
    n1 = document.getElementById("firstNumber").value;
    n2 = document.getElementById("secondNumber").value;

    document.getElementById("result").innerHTML = n1 / n2;
}